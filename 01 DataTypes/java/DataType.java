public class DataType {

  public static void main(String[] args) {
    boolean booleanType = false; // true or false

    char character = ' '; // 16-bit Unicode U +0000 to U +ffff

    byte byteNumber = 0; // 8-bit -128 to 127
    short shortNumber = 0; // 16-bit -32,768 to 32,767
    int integerNumber = 0; // 32-bit -2,147,483,648 to 2,147,483,647
    long longNumber = 0; // 64-bit -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807

    float floatNumber = 0.0f; // 32-bit
    double doubleNumber = 0.0; // 64-bit

  }
}